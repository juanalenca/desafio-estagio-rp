#3) Valor da variável SOMA

INDICE = 12
SOMA = 0
K = 1
while K < INDICE:
    K = K + 1
    SOMA = SOMA + K


"""

Neste código, K começa com 1 e incrementa até 12, 
e SOMA é a soma de todos os valores de K durante o loop.
Para calcular manualmente:

- K=1, SOMA=2 (K+1)
- K=2, SOMA=4 (SOMA+2)
- ...
- Até K=12, SOMA=78

Portanto, o valor final de SOMA é 78.
    
"""